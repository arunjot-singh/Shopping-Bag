import Foundation
import UIKit

struct Product {

    var brand: String!
    var type: String!
    var size: String!
    var color: String!
    var price: String!
    var image: String!
    
    init(brand: String, type: String, size: String, color: String, price: String, image: String){
        
        self.brand = brand
        self.type = type
        self.size = size
        self.color = color
        self.price = price
        self.image = image
        
    }
}


 













